print("=" * 68)
print("  PUSH 2 — |·| of |·| is the unit-self")
print("=" * 68)


def absM(X):                      # |X| = √(XᵀX), the radial part of polar decomp
    w, V = np.linalg.eigh(X.T @ X)
    return V @ np.diag(np.sqrt(np.clip(w, 0, None))) @ V.T


rng_examples = [("R", R), ("N", N), ("h", h), ("J", J), ("P", P)]
X = np.array([[1., 2.], [-3., 0.5]])
print(f"  idempotent?  | |X| | = |X|  for a generic X :  {np.allclose(absM(absM(X)), absM(X))}")
print(f"               |·|∘|·| = |·|  — abs is a fold whose defect is 0 (a P²=P)")
print()
print("  the unit-self = where |X| = I  (X orthogonal: XᵀX = I, X is its own unit):")
for name, M in rng_examples:
    aM = absM(M)
    is_unit = np.allclose(aM, I2)
    print(f"    |{name}| = {np.round(aM, 3).tolist()}   unit-self: {is_unit}")
print("  N, h, J are unit-self (|·|=I) — the rotation/involution generators.")
print("  R is NOT (|R|=√(R+I)) — production carries magnitude; the observer is pure unit.")
print()

