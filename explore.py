import sys
import io
import numpy as np

sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding="utf-8", errors="replace")

I2 = np.eye(2)
P = np.array([[0., 0.], [2., 1.]])
R = (P + P.T) / 2
N = (P - P.T) / 2
J = np.array([[1., 0.], [0., -1.]])
h = J @ N
fmt = lambda v: [round(float(x), 4) for x in sorted(np.real(v), reverse=True)]


def anti(s):   # X -> sX + Xs   (anticommutator action, symmetric / Jordan)
    return np.kron(I2, s) + np.kron(s.T, I2)


def comm(s):   # X -> sX - Xs   (commutator action, antisymmetric / Lie / adjoint)
    return np.kron(I2, s) - np.kron(s.T, I2)


print("=" * 68)
print("  PUSH 1 — {s,s} is self-action  =>  two primitive TYPES")
print("=" * 68)
print("  left-multiplication splits:  sX = ½(sX+Xs) + ½(sX−Xs)")
print("     symmetric  {s,·} = sX+Xs   (Jordan / anticommutator)")
print("     antisym    [s,·] = sX−Xs   (Lie / commutator / adjoint)")
print()
print(f"  the self-action we use, {{R,R}} = anti(R) − I :  spec = {fmt(np.linalg.eigvals(anti(R) - np.eye(4)))}")
print(f"  the OTHER one,         [R,·] = comm(R)     :  spec = {fmt(np.linalg.eigvals(comm(R)))}")
print(f"  for the observer:      {{N,N}} = anti(N) − I :  spec = {fmt(np.linalg.eigvals(anti(N) - np.eye(4)))}")
print(f"                         [N,·] = comm(N)     :  spec = {fmt(np.linalg.eigvals(comm(N)))}")
print("  same eigenvalues {+√5,0,0,−√5}, DIFFERENT eigenvectors:")
